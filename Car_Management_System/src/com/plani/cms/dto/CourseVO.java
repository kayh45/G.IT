package com.plani.cms.dto;

public class CourseVO {

	private int cour_no;
	private int s_place;
	private int e_place;
	private int distance;
	private String cour_purpo;
	
	public int getCour_no() {
		return cour_no;
	}
	public void setCour_no(int cour_no) {
		this.cour_no = cour_no;
	}
	public int getS_place() {
		return s_place;
	}
	public void setS_place(int s_place) {
		this.s_place = s_place;
	}
	public int getE_place() {
		return e_place;
	}
	public void setE_place(int e_place) {
		this.e_place = e_place;
	}
	public int getDistance() {
		return distance;
	}
	public void setDistance(int distance) {
		this.distance = distance;
	}
	public String getCour_purpo() {
		return cour_purpo;
	}
	public void setCour_purpo(String cour_purpo) {
		this.cour_purpo = cour_purpo;
	}
	
	
	
}
	